<?php


namespace App\Entity;


use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity
 * @ORM\Table(name="user")
 */
class User
{
    /**
     * @ORM\Id
     * @ORM\Column(type="string", unique=true, length=191)
     */
    private $email;

    /**
     * @ORM\Column(type="string")
     */
    private $password;
}